namespace FnsComposite.Interfaces
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Interface for IVehicleLookup. 
    /// This interface is between the object and the database table.
    /// All attributes have to be directly mapped to a column in the Vehicle table.
    /// </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    public interface IVehicleLookup
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the identifier of the automatic make model. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string AutoMakeModelId { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the year. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string Year { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the make. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string Make { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the model. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string Model { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the identifier of the reference. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string ReferenceId { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the upload key. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string UploadKey { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the archive flg. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string ArchiveFlg { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the identifier of the file tran log. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        string FileTranLogId { get; }
    }
}
